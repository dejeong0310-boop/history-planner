def create_frame(days, time_slots, session_key):
    st.write("")     # 공백
    st.subheader("2. 일정표")
    if session_key not in st.session_state:
        frame = pd.DataFrame(index=time_slots, columns=days).fillna(" ")
        st.session_state[session_key] = frame
    return st.session_state[session_key]

def add_schedule(days, time_slots, session_key):
    st.write("")
    st.subheader("3. 일정 추가하기")
    day = st.selectbox("날짜를 선택하세요.", options=days, key=f"day_{session_key}")
    time_slot = st.selectbox("시간대를 선택하세요.", options=time_slots, key=f"slot_{session_key}")
    user_input = st.text_area(f"{day} - {time_slot}에 추가할 일정을 입력하세요 :", key=f"input_{session_key}")

    if st.button('추가하기', key=f"add_{session_key}"):
        if user_input:
            st.session_state[session_key].at[time_slot, day] = user_input
            st.rerun()
        else:
            st.warning("일정을 입력해주세요!")

def table(x):
    x_df = pd.DataFrame(x).T
    x_df.columns = [f'{n+1}일차' for n in range(x_df.shape[1])]
